import 'dart:math';

void main() {
  int n1 = 100;
  int n2 = 200;

  //maxima and minima
  int maximum = max(n1, n2);
  print("Maximum is $maximum");

  int minimum = min(n1, n2);
  print("Minimum is $minimum");

  //power
  num power = pow(10, 2);
  print("The power $power");

  //squreroot
  num squareroot = sqrt(125);
  print("The squareroot $squareroot");
}
